package com.company.Tasks_Template.TasksForClasses.Itmathrepetitor_3;

public class Variable {
    double a;
    double b;

    public Variable(double a, double b) {
        this.a = a;
        this.b = b;
    }

    public double summ() {
        return a + b;
    }

    public double comparison() {
        return a > b ? a : b;
    }
}
